class Scores():

    #   method --------------------------------------------------------------
    # -----------------------------------------------------------------------
    def __init__(
        self,
        number=1,
        logger=None  
    ):
        self._len = number

        self._logger = logger

    #   method --------------------------------------------------------------
    # -----------------------------------------------------------------------
    def __len__(self):
        return self._len

    #   method --------------------------------------------------------------
    # -----------------------------------------------------------------------
    def __iter__(self):
        self._index = -1
        return self

    #   method --------------------------------------------------------------
    # -----------------------------------------------------------------------
    def __next__(self):
        if self._index < self._len-1:
            self._index += 1
            return self
        else:
            raise StopIteration

    #   method --------------------------------------------------------------
    # -----------------------------------------------------------------------
    def __repr__(self):
        pass

    #   method --------------------------------------------------------------
    # -----------------------------------------------------------------------
    def logger(self, log_str):
        if self._logger:
            self._logger.debug(log_str)
        return log_str